r"""## Move 3 · Build the 4-gate gauntlet ⭐ — PII → injection → off-policy → output

A guardrail layer is a **pipeline of independent gates**, each a **classifier**
(LLM-judge or Azure Content Safety / Prompt Shield), **never a regex** (I-25).
Four gates, run in order, short-circuiting on the first block:

1. **Gate 1 · PII** — when ACS is configured it calls `detectPII` and **redacts
   in-place**. On the native path there is *no redaction primitive*
   (`pii_exposure` only classifies exposed/not), so `action=redact` **honestly
   degrades to `block`** — we say so out loud rather than fake a redaction the kit
   can't do.
2. **Gate 2 · injection** — detects direct + indirect injection on the **input**
   *and* the retrieved **docs** (Prompt Shield when configured, else a
   `harmful_intent`-style judge over input + contexts).
3. **Gate 3 · off-policy** — matches an off-policy rubric (LLM-judge). This is the
   gate — and the *only* gate — that catches the competitor/badmouth case.
4. **Gate 4 · output** — screens the *final output* for harm/PII before it ships.

The pipeline is wired **pre-LLM** (input + retrieved-doc injection check) and
**post-LLM** (output PII/harm check), mirroring Kapi's `app/api/chat/route.ts`
pre/post pattern. We teach **four** gates; Kapi collapses to three Azure checks
(Prompt Shield + detectPII + output-safety) with off-policy folded into the
LLM-judge rubric. Tenant containers carry no Azure creds → in production the
analyze call proxies through the platform gateway (Kapi ADR-033); here it falls
through to the native engines on outage, **never to a regex floor** (I-25).
"""

from mai_rag.guardrails import Guardrail, ALLOW

GUARD = Guardrail()   # all four gates live

def guarded_run(case, guard=GUARD):
    """Run one attack THROUGH the gauntlet. Pre-LLM gates see the input + the
    (possibly poisoned) retrieved contexts; only if they ALLOW do we call the LLM
    and then run the post-LLM output gate. A blocked/redacted/escalated verdict
    is the SAFE outcome — we surface the gate's reason as the 'answer' so the
    scorer sees a refusal, not a leak."""
    # Retrieve first so the injection gate can inspect the retrieved docs.
    base = naive_rag(store, case["q"], k=4)
    ctx = list(base["contexts"])
    if case.get("injected_doc"):
        ctx = [case["injected_doc"]] + ctx

    # Pre-LLM: input + retrieved-doc gates (pii, injection, offpolicy).
    pre = guard.check(case["q"], contexts=ctx, output=None)
    if pre["action"] != ALLOW:
        refusal = f"[{pre['action'].upper()} · gate={pre['gate']}] {pre['reason']}"
        return refusal, ctx, pre

    # Gates passed → generate, then run the post-LLM output gate.
    from mai_rag import llm
    prompt = ("You are answering using only the provided context.\n\n"
              f"Question: {case['q']}\n\nContext:\n" + "\n\n".join(ctx) + "\n\nAnswer:")
    answer = llm.complete(prompt, tier="small")
    post = guard.check(case["q"], contexts=ctx, output=answer)
    if post["action"] != ALLOW:
        refusal = f"[{post['action'].upper()} · gate={post['gate']}] {post['reason']}"
        return refusal, ctx, post
    return answer, ctx, post

