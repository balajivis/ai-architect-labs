assert dest_recall == 1.0, "Structural gate must catch every destructive turn."

"""## Move 3 · Escalate on confidence and eval threshold — *the mid-cost fix*

Structural tags miss the turns that are dangerous because the **model is unsure**,
not because the tool is destructive. Add **dynamic triggers**: escalate(queue)
when `confidence < 0.7` OR a safety/relevance eval `< 0.8`.

**LLM self-confidence is NOT accuracy** — so the threshold is *tuned empirically*
against the golden set, not assumed. We sweep the confidence floor and read
precision vs recall on the `needs-escalate` slice, then pick the operating point
where we catch the misses without over-holding the routine turns.

(Mirrors Kapi's trigger DSL `confidence < 0.7` / `eval_score.{metric} < X` in
`evaluate.ts` — driven by LLM-judged scores, never a keyword list.)
"""

# The penny-stock low-confidence turn must route to escalate(queue) once triggers
# are on. confidence=0.41 < the 0.7 floor.
penny = next(a for a in ACTIONS if "penny stock" in a["q"].lower())
act = Action(tool="advice", risk=penny["tool_risk"], confidence=penny.get("confidence"))
d = checkpoint.checkpoint(act, use_structural=True, use_triggers=True, use_safety=False)
print(f"penny-stock turn (confidence={penny['confidence']}): → {d.action}  ·  {d.reason}")
assert d.action == checkpoint.QUEUE, "Low-confidence turn must escalate to the queue."

# Threshold sweep on the needs-escalate slice: precision vs recall of the
# confidence trigger. needs-escalate turns SHOULD be held; safe-autonomous reads
# should NOT. Sweep the floor and watch the trade-off.
escalate_turns = [a for a in ACTIONS if a["tag"] == "needs-escalate"]
safe_reads     = [a for a in ACTIONS if a["tag"] == "safe-autonomous"]
print(f"\nconfidence-floor sweep (needs-escalate={len(escalate_turns)}, safe-autonomous={len(safe_reads)}):")
print(f"  {'floor':>6} {'recall':>8} {'precision':>10}")
sweep = []
for floor in (0.3, 0.5, 0.7, 0.85, 0.95):
    def trig(a):
        c = a.get("confidence")
        return c is not None and c < floor
    held_esc  = sum(trig(a) for a in escalate_turns)       # true positives
    held_safe = sum(trig(a) for a in safe_reads)           # false positives
    recall    = held_esc / len(escalate_turns) if escalate_turns else 0.0
    precision = held_esc / (held_esc + held_safe) if (held_esc + held_safe) else 1.0
    sweep.append((floor, recall, precision))
    print(f"  {floor:>6.2f} {recall:>8.2f} {precision:>10.2f}")

# Pick the operating point: highest recall that keeps precision == 1.0 (no
# routine turn over-held). The default 0.7 floor is the sweet spot here.
best = max((s for s in sweep if s[2] >= 1.0), key=lambda s: s[1])
print(f"\nchosen operating point: floor={best[0]:.2f}  (recall={best[1]:.2f}, precision={best[2]:.2f})")
print("→ you TUNE the gate against the golden set; you don't guess it.")
assert best[1] == 1.0 and best[2] == 1.0, "Tuned trigger should catch every escalate turn with no over-block."

