"""## Move 2 · Tag tools read / write / destructive — *the free fix*

The cheapest, most surgical gate is **structural**: a per-tool `riskLevel`
declared at design time.

- **read** → proceed silently
- **write** → proceed, but **audit** (flag)
- **destructive** → **ALWAYS block**, overriding any per-step autonomy level

This is the one place a known-format check is legitimate — the tool's declared
risk tag is **structural metadata** (the `tool_risk` enum on each turn), *not* a
classification of content. (Models Kapi `lib/types/blueprint-manifest.ts:253`
riskLevel + the `executor.ts` read/write/destructive branch. **No regex** — the
tag is a declared enum, never inferred from the args string.)
"""

# Run the ACTION set through the STRUCTURAL gate alone (triggers + safety still
# off). Read the turn's declared tool_risk straight off the fixture — that IS the
# tool's riskLevel tag.
print(f"{'tool_risk':<12} {'tag':<16} {'decision':<9} reason")
print("-" * 78)
struct_decisions = []
for a in ACTIONS:
    act = Action(tool="agent", risk=a["tool_risk"], args={})
    d = checkpoint.checkpoint(act, use_structural=True, use_triggers=False, use_safety=False)
    struct_decisions.append((a, d))
    print(f"{a['tool_risk']:<12} {a['tag']:<16} {d.action:<9} {d.reason[:40]}")

# The structural gate catches every DESTRUCTIVE turn (the SQL DELETE) — but it
# can't see the low-confidence penny-stock query: tool_risk=read looks harmless.
def held(decision):  # a turn is "held" if the gate didn't proceed
    return decision.action != checkpoint.PROCEED

destructive = [(a, d) for a, d in struct_decisions if a["tool_risk"] == "destructive"]
