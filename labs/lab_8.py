# ── retry + backoff decorator (skeleton: you tune the budget + the math) ──
def with_retry(max_attempts: int = 3, base_delay: float = 0.05, timeout_code: int = -32001):
    """Wrap an mcp_call-style fn with timeout-aware retries + exponential backoff
    and jitter. Mirrors client.ts's retry-once-on-transient posture."""
    def deco(fn):
        @functools.wraps(fn)
        def wrapper(*a, **k):
            last = None
            for attempt in range(max_attempts):
                try:
                    return fn(*a, **k)
                except Exception as ex:   # transient: bridge/server hiccup
                    last = ex
                    if attempt == max_attempts - 1:
                        raise
                    # WIP: TODO — exponential backoff WITH jitter. Replace the
                    #   flat sleep with:  base_delay * (2 ** attempt) + random.uniform(0, base_delay)
                    delay = base_delay * (2 ** attempt) + random.uniform(0, base_delay)
                    time.sleep(delay)
            raise last  # unreachable, but explicit
        return wrapper
    return deco

