import urllib.request, json

SERVER_PORT = int(os.getenv("MCP_SERVER_PORT", "9000"))
MCP_URL = f"http://127.0.0.1:{SERVER_PORT}/mcp"

# ── the one helper every later move reuses: drive the Streamable-HTTP MCP endpoint
# the way server.mjs / Kapi's callTool do. It runs the CURRENT (2025-11-25)
# handshake: initialize → capture the Mcp-Session-Id → reuse it for every call.
# Streamable HTTP answers with either application/json or an SSE `data:` frame;
# we handle both. (Auth header is added from Move 5 on — see `extra_headers`.)
class _MCPSession:
    """A thin stateful client: one `initialize`, then `tools/list` / `tools/call`
    reuse the negotiated Mcp-Session-Id. Mirrors the host side your server serves."""
    def __init__(self, url: str, extra_headers: dict | None = None):
        self.url = url
        self.extra_headers = extra_headers or {}
        self.session_id: str | None = None
        self._id = 0

    def _post(self, body: dict):
        self._id += 1
        body = {"jsonrpc": "2.0", "id": self._id, **body}
        headers = {
            "Content-Type": "application/json",
            # Streamable HTTP requires the client to accept BOTH framings.
            "Accept": "application/json, text/event-stream",
            **self.extra_headers,
        }
        if self.session_id:
            headers["Mcp-Session-Id"] = self.session_id
        req = urllib.request.Request(self.url, data=json.dumps(body).encode(),
                                     headers=headers, method="POST")
        with urllib.request.urlopen(req, timeout=30) as r:
            sid = r.headers.get("Mcp-Session-Id")
            if sid:
                self.session_id = sid
            raw = r.read().decode("utf-8")
        # Streamable HTTP may answer as SSE: parse the last `data:` frame.
        if raw.lstrip().startswith("data:"):
            line = [ln for ln in raw.splitlines() if ln.startswith("data:")][-1]
            raw = line[len("data:"):].strip()
        return json.loads(raw) if raw else {}

    def initialize(self):
        return self._post({"method": "initialize", "params": {
            "protocolVersion": "2025-11-25",
            "capabilities": {},
            "clientInfo": {"name": "lab8-harness", "version": "0.1.0"},
        }})

def mcp_call(method: str, params: dict | None = None, session: "_MCPSession | None" = None,
             extra_headers: dict | None = None):
    """One-shot convenience: open a session (initialize), run one method, return
    the JSON-RPC response. Pass a live `session` to reuse the handshake instead."""
    s = session or _MCPSession(MCP_URL, extra_headers=extra_headers)
    if session is None:
        s.initialize()
    return s._post({"method": method, "params": params or {}})

