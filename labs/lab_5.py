"""## Move 1 · Open the judge's box

Most teams call an LLM-judge and read the number. That's a black box — and a black box you can't debug. The `mai_rag` engines are deliberately *transparent*: `faithfulness` doesn't return a vibe, it **breaks the answer into atomic claims and checks each against the retrieved context.**

Run it on one answer and read the `reasoning`. The number is the *summary*; the claim list is the *evidence*. When a metric looks wrong later, this is how you find out why.
"""

from mai_rag.evals import native

e = SUBJECTS[0]
print("QUESTION:", e.question)
print("ANSWER  :", e.answer[:260], "…\n")

# 1) The judge — a single overall score + one-sentence reasoning
